function cambiarFormLF() {
    var rf = document.getElementById("rf");
    var lf = document.getElementById("lf")
    var cf = document.getElementById("cf");
    cf.style.display = "none";
    rf.style.display = "none";
    lf.style.display = "block";
  }
  
